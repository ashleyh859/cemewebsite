$(document).ready(function () {
  $('#header1').click(function () {
    $('.accordion-content').not('#content1').addClass('hidden');
    $('.icon').not('#icon1').removeClass('rotate-icon');
    $('#content1').toggleClass('hidden')
    if ($('#content1').hasClass('hidden')) {
      $('#icon1').removeClass('rotate-icon');
    } else {
      $('#icon1').addClass('rotate-icon');
    }
  });

});

